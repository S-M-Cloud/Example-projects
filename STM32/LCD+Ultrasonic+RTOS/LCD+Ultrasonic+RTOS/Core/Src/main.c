void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // if the interrupt source is channel1
	{
		if (Is_First_Captured==0) // if the first value is not captured
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read the first value
			Is_First_Captured = 1;  // set the first captured as true
			// Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured==1)   // if the first is already captured
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = Difference  / 58.0; // Standard formula from HC-SR04 datasheet for cm;With timer running at 1MHz (1µs per tick)
			Is_First_Captured = 0; // set it back to false

			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC1);
		}
	}
}

void HCSR04_Read(void)
{
    // First ensure the echo line is not already active
    if (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_RESET) {
        // Reset the first captured flag
        Is_First_Captured = 0;
        
        // Generate 10 s trigger pulse
        HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);  // pull the TRIG pin HIGH
        // Use HAL_Delay for microsecond precision instead of vTaskDelay
        // Or better yet, use a precise microsecond delay if available
        for(uint16_t i=0; i<80; i++) { 
            __NOP(); // Each NOP is one CPU cycle
}
        HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  // pull the TRIG pin low
        
        // Enable interrupt for echo pin
        __HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC1);
    }
}



void ultrasonicTask(void *pvParameters) {
    float distance; // Local variable to store the distance

    while (1) {
        HCSR04_Read(); // Trigger the sensor reading
        vTaskDelay(pdMS_TO_TICKS(60)); // Wait for the measurement to complete (adjust as needed)

        // Copy the global distance to the local variable for queue safety
        distance = Distance;

        if (distance !=0) {

				xQueueSend(xdistanceQueue, &distance, 0);
 
        }

        vTaskDelay(pdMS_TO_TICKS(100)); // Delay between readings
    }
}


void lcdTask(void *pvParameters) {
    float distance;
    char distanceStr[16];  // Buffer to hold distance string
    
    while (1) {
        // Try to receive distance from the queue
        if (xdistanceQueue != NULL) {
            if (xQueueReceive(xdistanceQueue, &distance, 0) == pdTRUE) { 
                // Clear the display
                SSD1306_Clear();

                // Convert distance to string
                snprintf(distanceStr, sizeof(distanceStr), "Distance: %.1f cm", distance);

                // Set cursor to initial position
                SSD1306_GotoXY(7, 30);

                // Display the distance string
                SSD1306_Puts(distanceStr, &Font_7x10, SSD1306_COLOR_WHITE);

                // Update the screen
                SSD1306_UpdateScreen();
            }
        }

        // Small delay to prevent overwhelming the display
        vTaskDelay(pdMS_TO_TICKS(50));
    }
}


